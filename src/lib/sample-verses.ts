// Process raw verses into structured format with pre-computed letters
export const SAMPLE_VERSES: SampleVerse[] = RAW_VERSES.map((v, i) => {
  const fullVerse = `${v.h1} ۞ ${v.h2}`;
  return {
    id: i + 1,
    firstHemistich: v.h1,
    secondHemistich: v.h2,
    fullVerse,
    firstLetter: extractFirstLetter(fullVerse),
    lastLetter: extractLastLetter(fullVerse),
    poetName: v.poet,
    poetId: v.poetId,
    era: v.era,
  };
});

/**
 * Find a verse that starts with the given letter, excluding used IDs.
 */
export function findVerseByFirstLetter(
  letter: string,
  usedIds: number[]
): SampleVerse | null {
  const candidates = SAMPLE_VERSES.filter(
    (v) => v.firstLetter === letter && !usedIds.includes(v.id)
  );
  if (candidates.length === 0) return null;
  return candidates[Math.floor(Math.random() * candidates.length)];
}
